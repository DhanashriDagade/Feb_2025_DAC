class Literal
{
	public static void main(String args[])
	{

		int a=0b1010;
		System.out.println("binary conversion of (0b1010) is : "+a);
		int b=0765;
		System.out.println("Octal conversion of (0765) is : "+b);
		int c=0x1010;
		System.out.println("hexadecimal conversion of (0x1010) is : "+c);
		float d=123.456f;
		System.out.println("floating point conversion of (123.456) is : "+d);
		double e=123.456;
		System.out.println("floating point conversion of (123.456) is : "+e);
		double f=1e4;
		System.out.println("floating point conversion of (1e4) is : "+f);
	}
}
