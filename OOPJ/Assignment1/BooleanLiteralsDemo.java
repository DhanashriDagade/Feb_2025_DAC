class BooleanLiteralsDemo {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        boolean isMathHard = false;
        
        if (isJavaFun) {
            System.out.println("Java is fun to learn!");
        } else {
            System.out.println("Java is not fun.");
        }
        
        if (!isMathHard) {
            System.out.println("Math is easy!");
        } else {
            System.out.println("Math is hard.");
        }
    }
}
